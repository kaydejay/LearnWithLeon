/*obtain temperature in celsius (get value out of input)
convert from celsius to farenheit (convert the value)
display farenheit on document/on webpage (show the value to user)
((only on click))
*/

document.querySelector('#yell').addEventListener('click', convert)

function convert (){
    let temp = document.querySelector('#celsius').value
    temp = temp * 9/5 + 32
    document.querySelector('#placeToYell').innerText = temp
}
